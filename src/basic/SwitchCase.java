package basic;

public class SwitchCase {
    public static void main(String[]args){
        int squash=300;
        switch(squash){
            case 100:
                System.out.println("squash is less than 100");
                break;
            case 200:
                System.out.println("squash is less than 200");
                break;
                case 300:
                System.out.println("squash is equal to 300");
                break;
                default:
                    System.out.println("I will buy it no matter how much it cost");



        }
    }
}
